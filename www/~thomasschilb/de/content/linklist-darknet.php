							<h3 class="auto-style14">For 64 bit Debian</h3>
							<p class="auto-style12">
For 64bit, I was required to compile OpenSSL using “./config shared -fPIC”. The default location for the libraries then is /usr/local/ssl/lib64.</p><p></p>
							<h3 class="auto-style14">Libevent2</h3>
							<p class="auto-style12">
