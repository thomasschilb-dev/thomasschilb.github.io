							<div class="auto-style10"><p class="auto-style5">
								<font size="3">According to the 
	<a href="https://www.torproject.org/faq.html.en#RelayMemory">TorProject FAQ</a>, a Tor relay will use less memory if compiled with openBSD-malloc and the latest openSSL. This helps if you’re resources are limited, or if you run a high bandwidth node. Here’s how you can do that on a Debian system.</p>
								<p class="auto-style5">There’s also a nice list of additional information compiled for Tor relay operators on the mailing list: 
	<a href="http://archives.seul.org/or/relays/May-2010/msg00001.html">Tips for 
	running a Tor relay</a>.</font></p><h3 class="auto-style14">OpenBSD-Malloc CPU and memory tradeoff</h3>
								<p class="auto-style12">
According to the <a href="https://www.torproject.org/docs/faq.html.en#RelayMemory">Torproject FAQ</a>, there is a tradeoff between memory and CPU consumption: OpenBSD’s malloc implementation is lighter on memory, but uses more CPU. OpenSSL 1.0.0 saves memory without affecting CPU consumption. We can confirm this, so choose accordingly.</p><p></p>
								<h3 class="auto-style14">OpenSSL 1.0.0</h3>
								<p class="auto-style12">
As the default location for OpenSSL on Debian is /usr/bin , and the default configuration for the sources point to /usr/local/ssl/bin , there will be no conflicts between the packaged version and our manually compiled version – you can safely have both installed. You probably don’t even have to install openSSL at all, just point the Tor build rules to it, but I’ve installed it nevertheless:</span></p>
								<div class="text-align-right">
									<div class="auto-style5">
								<pre class="auto-style4"><span class="auto-style13">wget http://www.openssl.org/source/openssl-1.0.0.tar.gz</span>
tar xfz openssl-1.0.0.tar.gz
cd openssl-*
./config shared
make
make test
make install</pre></div>
								</div>
								<p class="auto-style12"><span class="auto-style1">(you should check openssl.org for later releases; 1.0.0 is current at the moment of writing)</span></p>
								<p class="auto-style10">
